<!-- SCRIPT SLIDER INTERAKTIF -->
<script>
    let currentTsbIndex = 0;
    const tsbSlides = document.querySelectorAll('#tsbSliderTrack > div');
    const totalTsbSlides = tsbSlides.length;
    const tsbTrack = document.getElementById('tsbSliderTrack');
    const tsbDots = document.querySelectorAll('.tsb-dot');
    let tsbInterval;

    function updateTsbSlider() {
        if(tsbTrack) {
            tsbTrack.style.transform = `translateX(-${currentTsbIndex * 100}%)`;
            tsbDots.forEach((dot, index) => {
                if (index === currentTsbIndex) {
                    dot.classList.remove('bg-white/40', 'w-2');
                    dot.classList.add('bg-white', 'w-5');
                } else {
                    dot.classList.remove('bg-white', 'w-5');
                    dot.classList.add('bg-white/40', 'w-2');
                }
            });
        }
    }

    function nextTsbSlide() { currentTsbIndex = (currentTsbIndex + 1) % totalTsbSlides; updateTsbSlider(); }
    function prevTsbSlide() { currentTsbIndex = (currentTsbIndex - 1 + totalTsbSlides) % totalTsbSlides; updateTsbSlider(); }
    function jumpToTsbSlide(index) { currentTsbIndex = index; updateTsbSlider(); resetTsbTimer(); }
    function startTsbTimer() { tsbInterval = setInterval(nextTsbSlide, 5000); }
    function resetTsbTimer() { clearInterval(tsbInterval); startTsbTimer(); }

    document.getElementById('tsbNextBtn')?.addEventListener('click', () => { nextTsbSlide(); resetTsbTimer(); });
    document.getElementById('tsbPrevBtn')?.addEventListener('click', () => { prevTsbSlide(); resetTsbTimer(); });

    document.addEventListener('DOMContentLoaded', () => {
        updateTsbSlider();
        startTsbTimer();
    });
</script>